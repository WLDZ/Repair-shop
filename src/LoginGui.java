    /**
     * getting login Info from main login Gui for the controller class
     * @return
     */
    public String[] getLoginInfo() {
    	
    	
    	loginInfo = new String[3];
    	
    	if(userNameTextField.getText().length()<=0)
    	{
    		loginInfo[0] = "nil";
    		
    	}
    	else
    	{
    		loginInfo[0] = userNameTextField.getText();
    	}
    	
    	//getting the data from the password field    	
    	if(passwordField.getText().length()<=0)
    	{
    		loginInfo[1] = "nil";
    		
    	}
    	else
    	{
    		loginInfo[1] = passwordField.getText();	
    	}
    	
    	//getting the data from the 'type of account' radio buttons
    	if(managerRadioButton.isSelected()==true ||
    			technicianRadioButton.isSelected()==true || receptionistRadioButton.isSelected()==true)
    	{
		    	if(bGroup.getSelection().getActionCommand().equals("rButton"))
		    	{
		    		loginInfo[2] = "receptionist";
		    	}
		    	else if(bGroup.getSelection().getActionCommand().equals("tButton"))
		    	{
		    		loginInfo[2] = "technician";
		    		
		    	} 
		    	else if(bGroup.getSelection().getActionCommand().equals("mButton"))
		    	{
		    		loginInfo[2] = "manager";
		    	}
    	}
    	else
    	{
    		loginInfo[2] =  "nil";
    		
    	}
    	
    	
		
    	return loginInfo;
    	
	}
    


